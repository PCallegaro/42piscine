#include <unistd.h>

void	ft_is_negative(int n)
{
	char	b;
	char	s;

	s = 'P';
	b = 'N';
	if (n < 0)
	{
		write(1, &b, 1);
	}
	else
	{
		write(1, &s, 1);
	}
}
